char *
execscript(char *cmd)
{
	FILE *fp;
	char retval[1025], *rv;

	memset(retval, 0, sizeof(retval));

	fp = popen(cmd, "r");
	if (fp == NULL)
		return smprintf("");

	rv = fgets(retval, sizeof(retval), fp);
	pclose(fp);
	if (rv == NULL)
		return smprintf("");
	retval[strlen(retval)-1] = '\0';

	return smprintf("%s", retval);
}

char * getvpnstatus(void)
{
	char *status;
	char *lockdown;

	status = execscript("mullvad status");

	if (strcmp(status, "Disconnected") == 0)
		return smprintf("%s", "0");

	lockdown = execscript("mullvad lockdown-mode get");

	if (strcmp(lockdown, "Network traffic will be allowed when the VPN is disconnected") == 0)
		return smprintf("%s", "1");

	return smprintf("%s", "2");
}

int
main(void)
{
	char *status;
	char *bat1;
	char *bat2;
	char *kbmap;
	char *tmlocal;
	char *network;
	char *vpn;

	if (!(dpy = XOpenDisplay(NULL))) {
		fprintf(stderr, "dwmstatus: cannot open display.\n");
		return 1;
	}

	for (;;sleep(10)) {
		bat1 = getbattery("/sys/class/power_supply/BAT0");
		bat2 = getbattery("/sys/class/power_supply/BAT1");
		kbmap = execscript("setxkbmap -query | grep layout | cut -d':' -f 2- | tr -d ' '");
		tmlocal = getlocaltime("%Y-%m-%d %H:%M (%Z)");
		network = execscript("nmcli networking connectivity");
		vpn = getvpnstatus();

		status = smprintf("N:%s  V:%s  K:%s  B:%s/%s  %s", network, vpn, kbmap, bat1, bat2, tmlocal);
		setstatus(status);

		free(network);
		free(vpn);
		free(kbmap);
		free(bat1);
		free(bat2);
		free(status);
		free(tmlocal);
	}

	XCloseDisplay(dpy);

	return 0;
}
